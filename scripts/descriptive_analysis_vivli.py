# Define resistance categories
df["non_susceptible_flag"] = df["cefiderocol_mic"] >= 4
df["clsi_resistant_flag"] = df["cefiderocol_mic"] >= 16

def clsi_category(mic):
    if pd.isna(mic):
        return pd.NA
    elif mic <= 4:
        return "S"
    elif mic == 8:
        return "I"
    elif mic >= 16:
        return "R"
    else:
        return pd.NA

df["clsi_category"] = df["cefiderocol_mic"].apply(clsi_category)

